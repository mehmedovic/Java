public class Problem5 {
	public static void backSlash(){
		System.out.println("\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\");
	}
	public static void forwardSlash(){
		System.out.println("//////////////////////");
	}
	public static void victory(){
		System.out.println("|| Victory is mine! ||");
	}
	public static void main(String[] args) {
		forwardSlash();
		victory();
		backSlash();
		victory();
		backSlash();
		victory();
		backSlash();
		victory();
		backSlash();
		victory();
		backSlash();
	}
} 